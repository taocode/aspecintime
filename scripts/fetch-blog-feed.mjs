/**
 * Fetches the latest WordPress posts from the site RSS feed
 * and writes data/thoughtfulspot.yaml for Hugo.
 */
import { existsSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const FEED_URL = 'https://aspecintime.com/feed/';
const LIMIT = 3;
const OUT = join(dirname(fileURLToPath(import.meta.url)), '..', 'data', 'thoughtfulspot.yaml');

function decode(text) {
  return text
    .replace(/<!\[CDATA\[([\s\S]*?)\]\]>/g, '$1')
    .replace(/&hellip;/g, '…')
    .replace(/&#8230;/g, '…')
    .replace(/&nbsp;/g, ' ')
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#8217;/g, "'")
    .replace(/&#8211;/g, '–')
    .replace(/<[^>]+>/g, '')
    .trim();
}

function tag(xml, name) {
  const re = new RegExp(`<${name}(?:\\s[^>]*)?>([\\s\\S]*?)<\\/${name}>`, 'i');
  const m = xml.match(re);
  return m ? decode(m[1]) : '';
}

function firstCategory(xml) {
  const re = /<category(?:\s[^>]*)?>([\s\S]*?)<\/category>/i;
  const m = xml.match(re);
  return m ? decode(m[1]) : '';
}

function formatDate(pubDate) {
  const d = new Date(pubDate);
  if (Number.isNaN(d.getTime())) return pubDate;
  return d.toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    timeZone: 'America/New_York',
  });
}

function yamlEscape(str) {
  if (/[:#\n"'&]/.test(str)) return JSON.stringify(str);
  return str;
}

async function main() {
  const res = await fetch(FEED_URL);
  if (!res.ok) throw new Error(`Feed fetch failed: ${res.status}`);
  const xml = await res.text();
  const items = [...xml.matchAll(/<item>([\s\S]*?)<\/item>/g)].map((m) => m[1]);

  const posts = items.slice(0, LIMIT).map((item) => ({
    title: tag(item, 'title'),
    link: tag(item, 'link'),
    date: formatDate(tag(item, 'pubDate')),
    category: firstCategory(item),
    excerpt: decode(tag(item, 'description')),
  }));

  const lines = [
    '# Generated by scripts/fetch-blog-feed.mjs — do not edit by hand',
    `fetchedAt: ${new Date().toISOString()}`,
    'posts:',
  ];

  for (const post of posts) {
    lines.push(`  - title: ${yamlEscape(post.title)}`);
    lines.push(`    link: ${yamlEscape(post.link)}`);
    lines.push(`    date: ${yamlEscape(post.date)}`);
    lines.push(`    category: ${yamlEscape(post.category)}`);
    lines.push(`    excerpt: ${yamlEscape(post.excerpt)}`);
  }

  writeFileSync(OUT, `${lines.join('\n')}\n`, 'utf8');
  console.log(`Wrote ${posts.length} posts to data/thoughtfulspot.yaml`);
}

main().catch((err) => {
  console.warn(`Blog feed fetch failed: ${err.message}`);
  if (!existsSync(OUT)) {
    process.exit(1);
  }
  console.warn('Using existing data/thoughtfulspot.yaml');
});
